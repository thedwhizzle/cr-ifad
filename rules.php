<h2>MONTHLY GOLF CZARS</h2>

<p>Each month of play, two members of the IFAD Men’s Golf Group shall serve as the Golf Czar and his Alternate. The Czar shall be responsible for:</p>
<ul>
	<li>Daily tee time pairings and selection of the game of the day, including managing handicap adjustments per game selected.</li>
	<li>Ensuring fairness of tee assignment (alternating starting holesshotgun).</li>
	<li><strong>The Acting Golf Czar must follow all Rules of Play as outlined herein.</strong></li>
	<li>In the case of a scheduling conflict that prohibits play at Chapel Ridge Golf Club, the Golf Czar shall be responsible for procuring teetimes at an agreeable alternative course and associated communications to members.</li>
	<li>Determination of daily wager winners.</li>
	<li>Allocation of daily wager winnings. and</li>
	<li>Coordination with Golf Pro Shop regarding sign up of players.</li>
</ul>
<p>The Golf Czar responsibilities shall rotate on a routine basis, usually monthly. The Golf Czar and his alternate are responsible to the Commissioner Emeritus and then the membership in general. <strong>Members will be asked to volunteer for the following season at the yearending awards party or when paying their membership fee.</strong></p>

