<h2>GENERAL RULES OF PLAY</h2>
<ul>
	<li style="font-weight: 800;">First day of play is Monday, February 15, 2016.</li>
	<li style="font-weight: 800;">Last day of regular play is Friday, September 30, 2016.</li>
	<li style="font-weight: 800;">Match Play October 1, 2016 through November 11, 2016.</li>
	<li style="font-weight: 800;">Membership Fee ($110) is due by February 1, 2016.</li>
	<li>USGA Rules of Play will be followed.</li>
	<li>Protecting the Field is Every Player’s Duty &amp; Responsibility: it is the responsibility of every group to monitor its’ actions with respect to the Rules of Golf and Pace of Play immediately upon recognition of such.</li>
	<li>A player will be limited to play to a handicap of 28 strokes, regardless of their posted handicap.</li>
	<li>A minimum of four (4) players must be present and commence play in order for a daily event to count as IFAD play.</li>
	<li>Daily Rules of Play may be altered by the Committee, or the Acting Golf Czar to respond to course conditions.</li>
	<li>When conditions are <strong>cart path only</strong>, play will be <strong>lift, clean, and place</strong> (defined as placing the ball within one club length no closer to the hole in the same condition of play as the original lie), in all areas except hazards.</li>
	<li>In the wintertime when course conditions deteriorate, the Golf Czar may declare that Preferred Lies be played defined as allowing a member to move his ball in the Fairway Only, one time – lift, clean and place (within one club length no closer to the hole in the same condition of play as the original lie).</li>
	<li>For the entire season the following Local Rules shall be in effect:
	<ul>
		<li><strong style="font-style: bold;">Divot Rule:</strong> when a ball comes to rest in a divot (sand filled or not) in the fairway, a player may take closest relief no nearer the hole.</li>
		<li><strong style="font-style: bold;">Rock Rule:</strong> when a ball comes to rest on rocks, either in the fairway or the rough, a player may take closest relief no nearer the hole.</li>
		<li><strong style="font-style: bold;">Leaf Rule:</strong> when a player is virtually certain that his ball has been hidden by leaves and is NOT out of bounds or in a hazard, a player may drop a ball and continue play without penalty of a stroke. The purpose of this Local Rule is to SPEED UP PLAY.</li>
	</ul>
	</li>
</ul>
	<p>Any player taking advantage of any of these Local Rules must consult with and gain agreement from his playing partners prior to proceeding with play. With the exception of LIFT CLEAN and PLACE (see Cart Path Only and Wintertime conditions above) “relief” shall be defined as DROPPING the ball consistent with USGA Rules.</p>
	<ul>
	<li>The ball must be holed out, unless the player has picked up due to reaching the hole maximum, is conceded the putt in match play, or is out of the hole in team play</li>
	<li>Actual Gross Scores must be recorded on the official scorecard with the exception of when a player reaches NET Triple Bogey</li>
	<li>A maximum score of NET Triple Bogey is allowed, after reaching which the player must pick up and assist his group in the completion of the hole. (e.g. if a player is a 9 handicap and is playing the first hole, which is currently the 9th handicap hole, Net Triple Bogey is a gross score of an 8, thus a net 7)</li>
	<li>Scores should be recorded in accordance with USGA Equitable Stroke Control standards</li>
	<li>Guests will be allowed to play in the group wager providing they have a valid USGA handicap.</li>
	<li><strong>Speed of play is defined as 4 Hours per round;</strong> The Acting Czar shall be responsible for addressing repeated instances of slow play with the offending player(s). Groups may be asked to record their starting and ending time on scorecards to be turned in by the Acting Czar.</li>
	<li>Ready Golf is the norm. Ready Golf is defined as being immediately behind the group in front of your group, not as being immediately ahead of the group behind your group; Ready Golfers should be at their ball in advance of their turn of play, have their club selected, play when ready in order to speed up play if your playing partners are not ready and read your putt in advance of your turn if it doesn’t interfere with others.</li>
	<li>Players may elect to play from any set of tees, providing USGA Handicap Rules are followed. The Golf Czar may dictate tees to be played during a scramble event.</li>
	<li>The applicable USGA Handicap Rules state that appropriate tee differentials
	should be applied when players play in the same contest from different tees.(USGA Handicap Section 9:C:i) Such play shall be conducted in accordance with the rules as set forth in the USGA Handicap Manual; Section 9:C:i.</li>
	<li>When courses other than Chapel Ridge are played the USGA Handicap Rules would also apply taking into account Course Ratings of different tees.</li>
	<li>To be included in play on a given day, the member must be present 15 minutes before the first teetime to allow for team formation, etc.</li>
	<li>The Acting Golf Czar should make his best efforts to determine that everyone that has arrived on a timely basis is included in that day’s game.</li>
	<li>The Committee may alter rules of play throughout the year as it deems necessary to maximize the enjoyment of the competition.</li>
</ul>
	<p style="font-weight: 800;">All suspected rules violations shall be resolved by the participating group on the spot, prior to advancing play. In the event that a disputed action is not resolved by the participants on the spot the appropriate procedure of the USGA rules shall be followed and the matter referred to the representative USGA PRO at the completion of play for resolution and the determination of the day’s competition. If a player accepts the imposition of a rules violation and continues play in accord with the USGA procedure no further action will be enacted. However; if a player defers a decision to the Pro and is adjudged to be in violation of the rules; and if such incidents occur three times in a season that player, upon receipt of a letter from the IFAD Committee shall be excluded from play for the remainder of the season, with no refund of monies being considered.</p>

<h2>WAGERING</h2>
<ul>
	<li>The daily group wager shall be $5 per man; an optional skins pool bet of $3 may be wagered when skins are played during individual play</li>
	<li>Individual wagers within foursomes may be negotiated before play commences</li>
	<li>The nature of the daily wager shall be the responsibility of the Golf Czar.</li>
	<li>Different forms of competition may incorporate handicap adjustments in accordance with USGA Recommendations (see chart)</li>
	<li>Once play has commenced, the daily wager must be paid by the member regardless of whether the member finishes the round; unless due to illness or injury to the member</li>
	<li>Team stroke play events may be formed when equal number of players are available for play (2-,3-, and 4-man teams)</li>
	<li>Individual stroke play events will be formed when there are an uneven number of players (e.g. 5,7,11,13,17,19,23)</li>
	<li>Skins play shall use 50% of handicap with NO strokes on par three’s. Handicaps shall be rounded down to an even number (e.g. if a player’s handicap is 7 that player shall get three shots total in the skins game and 7 shots total in the low net competition).</li>
	<li>Two-man match play or partner match play events may be formed when there are an even number of players (e.g. 4, 10, 14, 18)</li>
	<li>Group selection in <strong>team play</strong> may be ABCD to encourage fairness of team makeup and to speed play.</li>
	<li>Group selection in <strong>individual play</strong> may group players of similar playing abilities and handicaps.</li>
	<li>Group and player selection in <strong>match play</strong> may be through random ball toss; the intent for two-man match play is that everyone play a match against every other player by year’s end so it may end up being less than random at some point.</li>
	<li><strong>Blind Draws will not be allowed in competition in 2016; however, the averaging of net scores may be used for team games when odd number of players are present AND there is a consensus to play a team game over an individual game.</strong></li>
	<li>Payouts for all wagers will be made in accordance with the Payout and Points Chart, with the exception of the skins pool which will be a separate betting pool and which bet shall be optional. Players must opt-in prior to the commencement of play on any given day.</li>
</ul>

<h2>MATCH PLAY COMPETITION</h2>
<ul>
	<li>A year-ending true Double-Elimination Match Play tournament will be held at the end of the season. The tournament will be seeded based on a blind draw. Any player that has played during the regular season will be eligible to participate in the Match Play Tournament. A player’s handicap for the Match Play tournament will be the handicap as of the first day that the match is scheduled.</li>
	<li>The <strong>First Place Winner of the Match Play Championship</strong> shall receive temporary custody of the IFAD Champion’s Trophy, which rotates on an annual basis.</li>
	<li>The <strong>Top Finishers for the Match Play Championship</strong> shall have their names enshrined on the IFAD Group’s Annual Award Plaque, currently on display in the Chapel Ridge Clubhouse.</li>
</ul>

